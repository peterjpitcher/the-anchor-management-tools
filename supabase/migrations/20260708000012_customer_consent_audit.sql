INSERT INTO public.customer_consents (
  customer_id,
  channel,
  purpose,
  status,
  legal_basis,
  source,
  capture_method,
  consent_text_version,
  consent_text,
  metadata
)
SELECT c.id, legacy.channel, legacy.purpose, 'legacy', 'unknown', 'system_migration',
       'system_migration', 'legacy-system-state', NULL,
       jsonb_build_object('summary_field', legacy.summary_field, 'summary_value', true)
FROM public.customers c
CROSS JOIN LATERAL (
  VALUES
    ('sms', 'service', 'sms_opt_in', COALESCE(c.sms_opt_in, false)),
    ('sms', 'marketing', 'marketing_sms_opt_in', COALESCE(c.marketing_sms_opt_in, false)),
    ('email', 'marketing', 'marketing_email_opt_in', COALESCE(c.marketing_email_opt_in, false)),
    ('whatsapp', 'service', 'whatsapp_opt_in', COALESCE(c.whatsapp_opt_in, false)),
    ('whatsapp', 'marketing', 'marketing_whatsapp_opt_in', COALESCE(c.marketing_whatsapp_opt_in, false))
) AS legacy(channel, purpose, summary_field, is_enabled)
WHERE legacy.is_enabled = true
  AND NOT EXISTS (
    SELECT 1
    FROM public.customer_consents cc
    WHERE cc.customer_id = c.id
      AND cc.channel = legacy.channel
      AND cc.purpose = legacy.purpose
      AND cc.source = 'system_migration'
      AND cc.capture_method = 'system_migration'
      AND cc.status = 'legacy'
      AND cc.metadata->>'summary_field' = legacy.summary_field
  );

CREATE OR REPLACE VIEW public.customer_consent_legacy_gaps AS
SELECT
  c.id AS customer_id,
  c.first_name,
  c.last_name,
  c.mobile_number,
  c.email,
  field.summary_field,
  field.channel,
  field.purpose,
  field.summary_value
FROM public.customers c
CROSS JOIN LATERAL (
  VALUES
    ('sms_opt_in', 'sms', 'service', COALESCE(c.sms_opt_in, false)),
    ('marketing_sms_opt_in', 'sms', 'marketing', COALESCE(c.marketing_sms_opt_in, false)),
    ('marketing_email_opt_in', 'email', 'marketing', COALESCE(c.marketing_email_opt_in, false)),
    ('whatsapp_opt_in', 'whatsapp', 'service', COALESCE(c.whatsapp_opt_in, false)),
    ('marketing_whatsapp_opt_in', 'whatsapp', 'marketing', COALESCE(c.marketing_whatsapp_opt_in, false))
) AS field(summary_field, channel, purpose, summary_value)
WHERE field.summary_value = true
  AND NOT EXISTS (
    SELECT 1
    FROM public.customer_consents cc
    WHERE cc.customer_id = c.id
      AND cc.channel = field.channel
      AND cc.purpose = field.purpose
  );

GRANT SELECT ON public.customer_consent_legacy_gaps TO authenticated, service_role;
