) FROM PUBLIC;

GRANT EXECUTE ON FUNCTION public.record_customer_consent(
  UUID, TEXT, TEXT, TEXT, TEXT, TEXT, TEXT, TEXT, TEXT, UUID, TEXT, TEXT, TEXT, TEXT, TEXT, JSONB, BOOLEAN
